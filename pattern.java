class pattern
{
	public static void main(String[] args)
	 {
		int i;
		int j;
		for(i=4;i>=1;i--)
		{
			for(j=1;j<=i;j++)
			{
				System.out.print("*");
			}

			System.out.println("\n");
		}

		for(i=2;i<=4;i++)
		{
			for(j=1;j<=i;j++)
			{
				System.out.print("*");
			}

			System.out.println("\n");
		}
		
		
	}
}